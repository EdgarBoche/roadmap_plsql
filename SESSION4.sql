CREATE OR REPLACE PACKAGE NOMBRE_PACKAGE IS
    FUNCTION RETORNAR_SUMA(NUMNERO1 NUMBER, NUMNERO2 NUMBER) RETURN NUMBER;
    PROCEDURE ACTUALIZAR_FECHA(FECHA DATE);
END NOMBRE_PACKAGE;

CREATE OR REPLACE PACKAGE BODY NOMBRE_PACKAGE IS
    --///
    FUNCTION RETORNAR_SUMA(NUMNERO1 NUMBER, NUMNERO2 NUMBER) RETURN NUMBER IS
    SUMA NUMBER := 0;
    BEGIN
        FOR i IN NUMNERO1..NUMNERO2 LOOP
        SUMA := i + SUMA;
        DBMS_OUTPUT.PUT_LINE(SUMA);
        END LOOP;
        RETURN SUMA;
    END RETORNAR_SUMA;
    --//
    PROCEDURE ACTUALIZAR_FECHA(FECHA DATE) IS
    BEGIN
    UPDATE estudiante
    SET fecha_actualizacion = FECHA;
    END ACTUALIZAR_FECHA;
    --///
END NOMBRE_PACKAGE;

