create or replace PACKAGE METODOS_ESTUDIANTES IS
    FUNCTION TOTAL_ESTUDIANTES(P_SEXO VARCHAR2) RETURN NUMBER;
    PROCEDURE ACTUALIZACION_FECHA(P_ID NUMBER, P_FECHA DATE);
END METODOS_ESTUDIANTES;

create or replace PACKAGE BODY METODOS_ESTUDIANTES IS
 --///
    FUNCTION TOTAL_ESTUDIANTES(P_SEXO VARCHAR2) RETURN NUMBER IS 
        TOTAL NUMBER;
    BEGIN
        SELECT COUNT(ID) INTO TOTAL
            FROM ESTUDIANTE
            WHERE SEXO = P_SEXO;
    RETURN TOTAL;
    END TOTAL_ESTUDIANTES;
--//
    PROCEDURE ACTUALIZACION_FECHA(P_ID NUMBER, P_FECHA DATE) IS
    BEGIN  
    UPDATE estudiante
    SET FECHA_ACTUALIZACION = P_FECHA
    WHERE ID = P_ID;
    END ACTUALIZACION_FECHA;
--//
 END METODOS_ESTUDIANTES;
